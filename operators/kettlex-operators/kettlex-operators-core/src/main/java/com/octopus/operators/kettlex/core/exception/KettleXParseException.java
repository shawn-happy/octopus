package com.octopus.operators.kettlex.core.exception;

/** load json config exception */
public class KettleXParseException extends KettleXException {

  public KettleXParseException() {
    super();
  }

  public KettleXParseException(String message) {
    super(message);
  }

  public KettleXParseException(Throwable cause) {
    super(cause);
  }

  public KettleXParseException(String message, Throwable cause) {
    super(message, cause);
  }
}
