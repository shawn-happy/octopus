package com.octopus.kettlex.core.exception;

/** load json config exception */
public class KettleXJSONException extends KettleXException {

  public KettleXJSONException() {
    super();
  }

  public KettleXJSONException(String message) {
    super(message);
  }

  public KettleXJSONException(Throwable cause) {
    super(cause);
  }

  public KettleXJSONException(String message, Throwable cause) {
    super(message, cause);
  }
}
